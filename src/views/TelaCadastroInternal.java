    //ESSE METODO ATUALIZA A TABELA QUE Ã‰ ALIMENTADO PELO ARRAY
    public void atualizarTabela(){
        DefaultTableModel tb = (DefaultTableModel) jTabela.getModel();
        tb.setNumRows(0);
        for(int i=0; i < lista.size(); i++ ){
            tb.addRow(new Object[]{
                lista.get(i).getNome(),
                lista.get(i).getRg(),
                lista.get(i).getCpf(),
                lista.get(i).getCnpj(),
                lista.get(i).getDataNascimento(),
                lista.get(i).getSexo(),
                lista.get(i).getEstadoCivil(),
                lista.get(i).getTelefone()
            });
        }
    
    }
    
    //ESTE METODO REMOVE UM REGISTRO RECEBENDO COMO PARAMENTRO O INDICE 
    public void excluir(int index){
        for(int i=0; i < lista.size(); i++){
            if(index == i){
                lista.remove(i);
            }
        }
    }
    
    //ESSE METODO PEGA TODOS OS DADOS E COLOCA NA CAMPOS PARA QUE POSSAR SER EDITADO
    public void editar(int index){
        for(int i=0; i < lista.size(); i++){
            if(index == i){
                txtNome.setText(lista.get(i).getNome());
                txtRG.setText(lista.get(i).getRg());
                txtCPF.setText(lista.get(i).getCpf());
                txtCNPJ.setText(lista.get(i).getCnpj());
                txtDataNascimento.setText(lista.get(i).getDataNascimento());
                txtTelefone.setText(lista.get(i).getTelefone());
                txtEstadoCivil.setText(lista.get(i).getEstadoCivil());
                lista.remove(i);
            }
        }
    }
     //ESTE MEDOTO LIMPA TODOS OS CAMPOS APOS INSERIR UMA PESSOA
